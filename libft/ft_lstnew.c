#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*node;

	node = malloc(sizeof(t_list));
	if (!node)
		return (NULL);
	node->content = content;
	node->next = NULL;
	return (node);
}
// Return value The new node
// Description: Allocates and returns a new node. 
// The member variable ’content’ is initialized with the value ’content’. 
// The variable ’next’ is initialized to NULL.